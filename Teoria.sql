
-- DataBase = Un Conjunto de datos organizado para almacenar y luego gestionar.
-- SGDB (Sistema Gestor Database) Son por ejemplo mySQL, postreSQL, MariaDB :

-- Los SGDB son los que gestionan las bases de datos, siendo un intermediario encargandose de nuestra seguridad, integridad,etc.

-- Pk = Primary Key = columna la cual no se puede repetir, es un identificador
-- Fk ( Foreign Key ) = Columna para conectar 2 tablas.

-- Relaciones = Como pueden ser las relaciones entre 2 tablas, pueden ser : 1:n, n:1 y n_n
-- Donde n es cualquier numero.

-- Consultas = Nos permiten realizar operaciones en una tabla como insertar, actualizar, crear, renombrar, buscar,etc
-- SubConsultas = Consultas dentro de Consultar, Se usan entre Parentesis.


-- DER = Representaciones Graficas de una tabla en una Base de Datos.

-- Joins = Operaciones que nos permiten 2 tablas basandose en una condicion, Ejemplos de Joins :
-- Inner Join, Full Join, Left Join, Right Join, Cross Join.